let current = document.getElementsByClassName("breadcrumb")[0].innerText;


if(current == "Home"){
    var d = document.getElementsByClassName("page")[0];
    d.removeAttribute("href");

    const link = document.createElement("a");
    link.setAttribute('class', 'aiuti');
    link.setAttribute('href', '#contenuto')
    const node = document.createTextNode("salta la navigazione");
    link.appendChild(node);
    const element = document.getElementsByClassName("salto-navigazione")[0];
    element.appendChild(link);

    var cont = document.getElementsByTagName("main")[0];
    cont.setAttribute("id", "contenuto");
}

